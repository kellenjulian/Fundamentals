// Assignment operator
let x = 5;

// Addition assignment
x += 1; // x =x +1;


// Subtraction assignment
x -= 1;

// Multiplication assignment
x *= 1; // x = x* 1

// Division assignment
x /= 1; // x = x / 1

// Remainder (or Modulus) assignment
x %= 1; // x = x % 1;

// Exponential assignment
x **=1; //x = x ** 1

//********************************************************************************************* */
//Comparison operators

//Equal
console.log('3' == 3);

//Strict equal
console.log('3' === 3);

//Not equal
console.log('3' != 3);

//Strictly not equal
console.log('3' !== 3);

//Greater Than
console.log(3 > 2);

//Less Than
console.log(2 < 3);

//Less than or equal to
console.log(2 <= 3);

//Greater than or equal to
console.log(3 >= 2);

//Or
2 || 3;

//And
2 && 3;



